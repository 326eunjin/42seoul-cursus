#include <unistd.h>

void	func(char *a)
{
	if (a[4] > '9')
	{
		if (a[3] == '9')
		{
			if (a[1] == '9')
			{
				a[0]++;
				a[1] = '0';
			}
			else
			{
				a[1]++;
			}
			a[3] = a[0];
			a[4] = a[1] + 1;
		}
		else
		{
			a[3]++;
			a[4] = '0';
		}
	}
}

void	ft_print_comb2(void)
{
	char a[5];

	a[2] = 32;
	a[0] = '0';
	a[1] = '0';
	a[3] = '0';
	a[4] = '1';
	while (a[3] <= '9')
	{
		while (a[4] <= '9')
		{
			write(1, a, 5);
			if (!((a[0] == '9') && (a[1] == '8') &&
						(a[3] == '9') && (a[4] == '9')))
				write(1, ", ", 2);
			a[4]++;
		}
		func(a);
	}
}
