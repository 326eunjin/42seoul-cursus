int	ft_atoi(char *str)
{
	int neg;
	int nbr;
	int i;

	neg = 0;
	nbr = 0;
	i = 0;
	while ((str[i] == 9) || (str[i] == 10) || (str[i] == 11)
			|| (str[i] == 12) || (str[i] == 13) || (str[i] == 32))
		i++;
	while ((str[i] == '-') || (str[i] == '+'))
	{
		if (str[i] == '-')
			neg++;
		i++;
	}
	while ((str[i] >= '0') && (str[i] <= '9'))
	{
		nbr = nbr * 10 + (str[i] - 48);
		i++;
	}
	if (neg % 2 == 1)
		return ((-1) * nbr);
	else
		return (nbr);
}
